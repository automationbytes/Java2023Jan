package Day2;

public class BitwiseOperator {
	public static void main(String[] args) {

		int a = 5; //  0101
		int b = 9; //  1001
				   
					//  0001 -> 1 and
				   //  1101
		System.out.println(a & b);
		
		System.out.println(a | b);
		
		/*
		 * binary and
		 * 
		 * 1 and 1 = 1
		 * 1 and 0 = 0
		 * 0 and 1 = 0
		 * 0 and 0 = 0
		 * 
		 * binary or
		 * 1 or 1 = 1
		 * 1 or 0 = 1
		 * 0 or 1 = 1
		 * 0 or 0 = 0
		 * 
		 * 
		 */
	}
}
