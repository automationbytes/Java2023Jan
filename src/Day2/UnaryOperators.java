package Day2;

public class UnaryOperators {
public static void main(String[] args) {
	int i = 10;
	//i++
	//assignment -> increment
	
	System.out.println(i++); // 10
	System.out.println(i); // 11
	
	//++i
	//increment-> assignment
	int j = 10;
	System.out.println(++j);//11
	System.out.println(j);//11
}
}
